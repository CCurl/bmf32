    outer("    then ')' emit ;");
    // outer("");
    outer(": .word ( de-- ) cell+ 2 + ztype ;");
    outer(": words ( -- ) +L last x! 0 y! 0 z! begin");
    outer("        x@ dict-end < if0 '(' emit z@ . .\" words)\" -L exit then");
