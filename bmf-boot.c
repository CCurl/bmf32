    outer(": rdrop ( -- ) r> drop ; inline");
    outer(": tuck  ( a b--b a b )   swap over ; inline");
    outer(": nip   ( a b--b )       swap drop ; inline");
    outer(": ?dup ( n--n n|0 )  -if dup then ;");
    outer(": 2dup  ( a b--a b a b ) over over ; inline");
    outer(": 2drop ( a b-- )        drop drop ; inline");
    outer(": -rot ( a b c--c a b )  swap >r swap r> ;");
    outer(": cell+ ( a--a' ) cell + ; inline");
